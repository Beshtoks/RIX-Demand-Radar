    override fun getDashboardState(): DashboardUiState {
        val now = LocalTime.now()
        val hour = now.hour
        val updated = "Обновлено: ${now.format(timeFormatter)}"

        return when {
            hour in 6..9 -> buildMorningState(updated)
            hour in 10..14 -> buildDayState(updated)
            hour in 15..19 -> buildEveningState(updated)
            else -> buildLateState(updated)
        }
    }

